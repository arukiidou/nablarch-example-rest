CREATE TABLE `PROJECT` (
  `PROJECT_ID` int NOT NULL COMMENT 'プロジェクトID',
  `PROJECT_NAME` varchar(128) NOT NULL COMMENT 'プロジェクト名',
  `PROJECT_TYPE` varchar(128) NOT NULL COMMENT 'プロジェクト種別',
  `PROJECT_CLASS` varchar(128) NOT NULL COMMENT 'プロジェクト分類',
  `PROJECT_START_DATE` date DEFAULT NULL COMMENT 'プロジェクト開始日付',
  `PROJECT_END_DATE` date DEFAULT NULL COMMENT 'プロジェクト終了日付',
  `CLIENT_ID` int DEFAULT NULL COMMENT '顧客ID',
  `PROJECT_MANAGER` varchar(128) DEFAULT NULL COMMENT 'プロジェクトマネージャー',
  `PROJECT_LEADER` varchar(128) DEFAULT NULL COMMENT 'プロジェクトリーダー',
  `USER_ID` int DEFAULT NULL COMMENT 'ユーザID',
  `NOTE` varchar(512) DEFAULT NULL COMMENT '備考',
  `SALES` int DEFAULT NULL COMMENT '売上高',
  `COST_OF_GOODS_SOLD` int DEFAULT NULL COMMENT '売上原価',
  `SGA` int DEFAULT NULL COMMENT '販管費',
  `ALLOCATION_OF_CORP_EXPENSES` int DEFAULT NULL COMMENT '本社配賦',
  PRIMARY KEY (`PROJECT_ID`) /*T![clustered_index] CLUSTERED */,
  KEY `FK_CLIENT_ID` (`CLIENT_ID`),
  KEY `FK_USER_ID` (`USER_ID`),
  CONSTRAINT `FK_CLIENT_ID` FOREIGN KEY (`CLIENT_ID`) REFERENCES `CLIENT` (`CLIENT_ID`),
  CONSTRAINT `FK_USER_ID` FOREIGN KEY (`USER_ID`) REFERENCES `SYSTEM_ACCOUNT` (`USER_ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_bin COMMENT='プロジェクト';